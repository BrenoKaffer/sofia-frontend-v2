import { NextRequest, NextResponse } from 'next/server';

// Configura√ß√£o do Backend SOFIA
const SOFIA_BACKEND_URL = 'http://localhost:3001/api';

export async function GET(request: NextRequest) {
  try {
    console.log('üöÄ Iniciando API de hist√≥rico de roletas...');
    
    // Verificar autoriza√ß√£o
    const authHeader = request.headers.get('authorization');
    if (!authHeader || !authHeader.startsWith('Bearer ')) {
      return NextResponse.json(
        { error: 'Token de autoriza√ß√£o necess√°rio' },
        { status: 401 }
      );
    }

    // Extrair par√¢metros da query
    const { searchParams } = new URL(request.url);
    const limit = parseInt(searchParams.get('limit') || '100');
    const tableId = searchParams.get('table_id');

    // Buscar hist√≥rico de roletas do backend SOFIA
    console.log('üîç Buscando hist√≥rico de roletas do backend SOFIA...');
    
    const historyUrl = new URL(`${SOFIA_BACKEND_URL}/roulette-history`);
    if (limit) {
      historyUrl.searchParams.set('limit', limit.toString());
    }
    if (tableId) {
      historyUrl.searchParams.set('table_id', tableId);
    }

    const response = await fetch(historyUrl.toString(), {
      method: 'GET',
      headers: {
        'Content-Type': 'application/json',
        'Authorization': authHeader,
      },
    });

    if (!response.ok) {
      console.warn('‚ö†Ô∏è Erro ao buscar hist√≥rico do backend:', response.status, response.statusText);
      // Retornar array vazio em caso de erro
      return NextResponse.json([]);
    }

    const historyData = await response.json();
    console.log('‚úÖ Hist√≥rico de roletas recebido do backend SOFIA:', historyData?.length || 0);

    // Garantir que os dados sejam um array
    const sanitizedData = Array.isArray(historyData) ? historyData : [];

    return NextResponse.json(sanitizedData);
  } catch (error) {
    console.error('Erro na API de hist√≥rico da roleta:', error);
    return NextResponse.json(
      { error: 'Erro interno do servidor' },
      { status: 500 }
    );
  }
}